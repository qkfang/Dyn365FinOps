[CmdletBinding()]
param()

$ErrorActionPreference = "Stop"
Trace-VstsEnteringInvocation $MyInvocation
try
{
    #get the testsuite files based on the built dlls
    Write-Host "Fetching test suites to run"
    $xppBinPath = Get-VstsInput -Name "XppBinariesPath" -Require
    $xppBinSearch = Get-VstsInput -Name "XppBinariesSearch" -Default "*"
    if ($xppBinSearch.Contains("`n"))
    {
        [string[]]$xppBinSearch = $xppBinSearch -split "`n"
    }
    Assert-VstsPath -LiteralPath $xppBinPath -PathType Container
    $potentialPackages = Find-VstsMatch -DefaultRoot $xppBinPath -Pattern $xppBinSearch | Where-Object { (Test-Path -LiteralPath $_ -PathType Container) }
    #$potentialPackages = @("C:\AOSService\PackagesLocalDirectory\AAAAUTest1")
    
    if ($potentialPackages.Length -gt 0)
    {
        $compilerPath = Get-VstsInput -Name "CompilerPath" -Require
        $platformPath = Get-VstsInput -Name "PlatformPath" -Require
        Assert-VstsPath -LiteralPath $compilerPath -PathType Container
        Assert-VstsPath -LiteralPath $platformPath -PathType Container
        $refPaths = Get-VstsInput -Name "ReferencePaths"
        $platformPath = Join-Path -Path $platformPath -ChildPath "ref"
        $platformPath = Join-Path -Path $platformPath -ChildPath "net40"
        Assert-VstsPath -LiteralPath $platformPath -PathType Container
        Import-Module "$PSScriptRoot\TestRequestTSXFetch.ps1" -Force
        $testSuiteDirectory = Get-TestTSXDirectory -potentialPackages $potentialPackages -compilerPathInput $compilerPath -platformPathInput $platformPath -referencePaths $refPaths

        #get connection info from params to create a CRMServiceClient object
        $cloudPackagePlatVersion = Get-VstsInput -Name "CloudPackagePlatVersion" -Require
        $cloudPackageAppVersion = Get-VstsInput -Name "CloudPackageAppVersion" -Require
        $regexVersion = '^\d+\.\d+\.\d+\.\d+$'
        if ($cloudPackagePlatVersion -notmatch $regexVersion) 
        {
            throw "Invalid platform version: $cloudPackagePlatVersion"
        }
        if ($cloudPackageAppVersion -notmatch $regexVersion) 
        {
            throw "Invalid application version: $cloudPackageAppVersion"
        }
        
        $connString = Get-VstsInput -Name 'PowerPlatformEnvironment' -Require
        #call the unit test operation assume there is svcCLone object and tsx file
        Write-Host "Creating cloud runtime unittest request"
        $PSVersionTable
        Import-Module "$PSScriptRoot\CloudRuntimeUnitTestRequest.ps1" -Force
        Create-CloudRuntimeUnitTestRequest -connectionString "$connString" -platVersionNumber "$cloudPackagePlatVersion" -appVersionNumber "$cloudPackageAppVersion" -testSuiteDirectory "$testSuiteDirectory"
    }
    else
    {
        throw "No matching potential packages found."        
    }
}
finally
{
    Trace-VstsLeavingInvocation $MyInvocation
}

